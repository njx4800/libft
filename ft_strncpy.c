#include "libft.h"

char *ft_strncpy(char *dest, const char *src, size_t len)
{
	char *d1;

	d1 = dest;
	while(len--)
	{
		if(*src != '\0')
			*d1++ = *src++;
		else
			*d1++ = '\0';
	}
	return(dest);
}
