#include "libft.h"

int		ft_wdcount(char const *s, int i, char c)
{
	while (*s == c)
		s++;
	while (*s != '\0')
	{
		while (*s && *s != c)
			s++;
		while (*s == c)
			s++;
		i++;
	}
	return (i);
}
